
#ATRIBUIÇÃO SIMPLES =

saldo = 500
print(saldo)

#SOBESCREVEU A VARIAVEL REPASSANDO NOVO VALOR 

saldo = 200
print(saldo)

#ATRIBUIÇÃO COM ADIÇÃO +=

saldo += 10
print(saldo)

#ATRIBUIÇÃO COM SUBTRAÇÃO -=

saldo -= 5
print(saldo)

#ATRIBUIÇÃO COM DIVISAO INTEIRO //=

saldo //= 2
print(saldo)

#ATRIBUIÇÃO COM MULTIPLICAÇÃO *=

saldo *= 10
print(saldo)

#ATRIBUIÇÃO COM DIVISAO /=

saldo /= 2
print(saldo)

#ATRIBUIÇÃO COM MODULO %=

saldo %= 3
print(saldo)

#ATRIBUIÇÃO COM EXPONENCIAÇÃO **=

saldo **= 4
print(saldo)
