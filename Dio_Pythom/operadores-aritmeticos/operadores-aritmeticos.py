 #ADIÇÃO +

produto_1 = 20
produto_2 = 10

print(produto_1 + produto_2 )

#SUBTRAÇÃO - 

print(produto_1 - produto_2)

#MULTIPLICAÇÃO * 

print(produto_1 * produto_2)


#DIVISÃO /

print(produto_1 / produto_2)

#DIVISÇÃO INTEIRO //

print(produto_1 // produto_2)

#MODULO %

print(produto_1 % produto_2)

#EXPONENCIAÇÃO **

print(produto_1 ** produto_2)

#Exemplos 

x = 10 + 5 * 4
z = 10 / 2 + 25 * 2 - 2 ** 2

print(x)
print(z)

#FORÇANDO ORDEM DE PRECEDENCIA 

y = (10 + 5) * 4

print(y)

